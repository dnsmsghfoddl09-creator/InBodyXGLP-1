const kolMap = new Map(ENTERPRISE_KOLS.map((k) => [k.id, k]));

export function getEnterpriseKol(id: KolId): EnterpriseKol | undefined {
  return kolMap.get(id);
}

export function getAllEnterpriseKols(): EnterpriseKol[] {
  return ENTERPRISE_KOLS;
}

export function getKolsForCountry(countryId: CountryId): EnterpriseKol[] {
  return ENTERPRISE_KOLS.filter((k) => k.countryId === countryId);
}

export function getKolsByIds(ids: KolId[]): EnterpriseKol[] {
  return ids.map((id) => kolMap.get(id)).filter((k): k is EnterpriseKol => k !== undefined);
}
